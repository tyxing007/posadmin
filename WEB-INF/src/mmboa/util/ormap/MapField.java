public class MapField {
    public String objField;

    public String tableField;

    public String objType;
    
    
    /**
     * @return Returns the objField.
     */
    public String getObjField() {
        return objField;
    }
    /**
     * @param objField The objField to set.
     */
    public void setObjField(String objField) {
        this.objField = objField;
    }
    /**
     * @return Returns the objType.
     */
    public String getObjType() {
        return objType;
    }
    /**
     * @param objType The objType to set.
     */
    public void setObjType(String objType) {
        this.objType = objType;
    }
    /**
     * @return Returns the tableField.
     */
    public String getTableField() {
        return tableField;
    }
    /**
     * @param tableField The tableField to set.
     */
    public void setTableField(String tableField) {
        this.tableField = tableField;
    }
}
